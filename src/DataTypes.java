public class DataTypes {
  public static void main(String args[]) {
    // NUMERICS
    byte a = 20; // 8bit -> -2^7 to 2^7-1
    short b = 3456; // 16bit -> -2^15 to 2^15-1
    int c = 3453622; // 32 bit -> -2^31 to 2^31-1
    long d = 45_574_354_86L; // 64 BIT -> -2^63 TO 2^63-1

    // DECIMALS
    float e = 45.78F; // 32 bit -> -2^31 to 2^31-1
    double f = 567884.766; // 64 bit -> -2^63 to 2^63-1

    // CHARACTER
    char g = 't'; // 16 bit -> -2^15 to 2^15-1

    // BOOLEAN
    boolean h = true; // 1 bit or false

  }
}
