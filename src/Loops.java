public class Loops {
  public static void main(String args[]) {
    // LOOPS
    // While, Do while, for

    // For Loop
    for (int i = 0; i <= 10; i++) {
      System.out.println("Hello world");
    }
    // While Loop
    int a = 0;
    while (a <= 10) {
      System.out.println("While hello");
      a++;
    }
    // Do While Loop
    int b = 0;
    do {
      System.out.println("Do While Hello");
      b++;
    } while (b <= 0);

  }
}
